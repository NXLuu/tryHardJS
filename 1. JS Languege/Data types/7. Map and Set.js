/* 
    Map:
    new Map() - tạo một map
    map.set(key, value) - lưu giá trị của key
    map.get(key) - trả về giá trị của key
    map.has(key) - trả về true nếu key tồn tại, false nếu ngược lại
    map.delete(key) - xoá giá trị của key
    map.clear() - xoá mọi thứ của map
    map.size - trả về số lượng hiện tại phần tử

    map.keys() - trả về một iterable tất cả key
    map.values() - trả về một iterable tất vả values
    map.entries() - trả về một iterable cho [key, value]
*/

